@app.post("/webhook")
def webhook():
    """Receive Telegram updates from Telegram servers."""
    try:
        update_data = request.get_json(force=True)
        logger.info(f"ğŸ“© Incoming Telegram update: {update_data}")

        # Schedule processing asynchronously in background
        async def process():
            update = Update.de_json(update_data, application.bot)
            await application.process_update(update)

        try:
            loop = asyncio.get_event_loop()
        except RuntimeError:
            loop = asyncio.new_event_loop()
            asyncio.set_event_loop(loop)

        loop.create_task(process())

        # Respond instantly so Render doesn't timeout
        return "OK", 200

    except Exception as e:
        logger.exception(f"âŒ Webhook error: {e}")
        return jsonify({"error": str(e)}), 500


